package JAVA_BASICS;

public class IfElseConditionExample {
		public static void main(String[] args) {
			boolean isAvailable = false;
			String id = "A22";
			if(isAvailable) {
				System.out.print("Seat "+id+" is available");
			}
			else {
				System.out.print("Seat is not available");
			}
		}
}
